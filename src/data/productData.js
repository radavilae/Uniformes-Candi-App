export const getProductData = (category, index) => {
  const products = productData[category];
  if (!products || !products[index]) {
    return {
      code: "N/A",
      name: "Producto no disponible",
      description: "Información no disponible",
      material: "N/A",
      sizes: ["N/A"],
      colors: ["N/A"],
      price: "Consultar",
      features: ["N/A"],
      care: "N/A"
    };
  }
  return products[index];
};

